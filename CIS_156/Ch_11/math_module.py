# This program does a factorial an cubing operation on a given number

import math

our_num = int(input('\nEnter a number: '))

print(f'{our_num} factorial is {math.factorial(our_num)}')
print(f'{our_num} raised to the power of 3 is {math.pow(our_num, 3)}\n')